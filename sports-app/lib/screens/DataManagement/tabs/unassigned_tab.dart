  Future<void> _assignTrainer(Trainer t) async {
    final dm = DataManagementScope.of(context);
    if (t.id == null || _selectedDivision == null) return;

    final ok =
    await dm.assignTrainerDivision(t.id!, _selectedDivision);

    if (!mounted) return;
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
          content: Text(ok ? 'Trainer assigned.' : 'Assignment failed.')),
    );
  }

  Future<void> _assignPlayerDivision(Player p) async {
    final dm = DataManagementScope.of(context);
    if (p.id == null || _selectedDivision == null) return;

    final ok = await dm.assignPlayerRelations(
      playerId: p.id!,
      divisionId: _selectedDivision,
      parentId: null,
      trainerId: null,
    );

    if (!mounted) return;
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
          content: Text(
              ok ? 'Player assigned to division.' : 'Assignment failed.')),
    );
  }

  Future<void> _assignParentBulk() async {
    final dm = DataManagementScope.of(context);
    if (_selectedParent == null || _selectedPlayerIds.isEmpty) return;

    final ok = await dm.assignParentToPlayers(
      parentId: _selectedParent!,
      playerIds: _selectedPlayerIds.toList(),
    );

    if (!mounted) return;
    ScaffoldMessenger.of(context).showSnackBar(
      SnackBar(
          content:
          Text(ok ? 'Assigned successfully.' : 'Assignment failed.')),
    );

    if (ok) {
      setState(() {
        _selectedPlayerIds.clear();
      });
    }
  }
