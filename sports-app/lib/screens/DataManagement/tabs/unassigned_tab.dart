  Widget _playersMultiSelect(List<Player> list) {
    if (list.isEmpty) return _empty('No players available.');
    return Column(
      children: [
        for (final p in list)
          SoftCard(
            padding: EdgeInsets.zero,
            margin: const EdgeInsets.only(bottom: 12),
            child: CheckboxListTile(
              value: p.id != null && _selectedPlayerIds.contains(p.id),
              onChanged: (v) {
                if (p.id == null) return;
                setState(() {
                  if (v == true) {
                    _selectedPlayerIds.add(p.id!);
                  } else {
                    _selectedPlayerIds.remove(p.id!);
                  }
                });
              },
              secondary: CircleAvatar(
                backgroundColor: Colors.orange.withOpacity(0.15),
                child: Icon(Icons.sports_soccer, color: Colors.orange, size: 20),
              ),
              title: Text(
                p.nom ?? 'Player',
                style: const TextStyle(fontWeight: FontWeight.w700),
              ),
              subtitle: Text(
                'Division: ${p.divisionId != null ? 'Assigned' : 'None'}',
                style: TextStyle(fontSize: 12, color: Colors.grey[600]),
              ),
            ),
          )
      ],
    );
  }

  Widget _bulkAssignButton(BuildContext context,
      {required String label}) {
    final can = _selectedParent != null && _selectedPlayerIds.isNotEmpty;
